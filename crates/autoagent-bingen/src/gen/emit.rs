// Return-type classification (drives marshaling in every backend).
// ---------------------------------------------------------------------------

/// How a symbol's neutral-wrapper output (always a JSON string from `bind`) maps
/// to a host value.
enum Ret {
    /// `version` — parse the string as an integer.
    Number,
    /// `init` — parse the string as a boolean.
    Boolean,
    /// `void` — discard the (empty) string.
    Void,
    /// Raw text passthrough (e.g. rendered TOML, a patch body) — `returns: string`.
    Str,
    /// A JSON document (object/array/struct) — parse to a host value.
    Json,
}

fn classify(s: &Symbol) -> Ret {
    match s.returns {
        "number" => Ret::Number,
        "boolean" => Ret::Boolean,
        "void" => Ret::Void,
        "string" => Ret::Str,
        _ => Ret::Json,
    }
}

/// snake_case -> camelCase for JS/TS function names.
fn camel(snake: &str) -> String {
    let mut out = String::new();
    let mut up = false;
    for c in snake.chars() {
        if c == '_' {
            up = true;
        } else if up {
            out.extend(c.to_uppercase());
            up = false;
        } else {
            out.push(c);
        }
    }
    out
}

fn priv_str(p: Privilege) -> &'static str {
    match p {
        Privilege::Read => "read",
        Privilege::Mutate => "mutate",
    }
}

fn kind_str(k: Kind) -> &'static str {
    match k {
        Kind::Sync => "sync",
        Kind::Async => "async",
    }
}

// ---------------------------------------------------------------------------
// JSON schema of the full surface contract (FR-11).
// ---------------------------------------------------------------------------

fn schema_json() -> String {
    let syms: Vec<String> = SURFACE
        .iter()
        .map(|s| {
            let args: Vec<String> = s
                .args
                .iter()
                .map(|a| format!("{{ \"name\": \"{}\", \"type\": \"{}\" }}", a.name, a.ty))
                .collect();
            format!(
                "    {{ \"name\": \"{}\", \"kind\": \"{}\", \"privilege\": \"{}\", \"args\": [{}], \"returns\": \"{}\" }}",
                s.name,
                kind_str(s.kind),
                priv_str(s.privilege),
                args.join(", "),
                s.returns
            )
        })
        .collect();
    format!(
        "{{\n  \"version\": \"1.0.0\",\n  \"symbols\": [\n{}\n  ]\n}}\n",
        syms.join(",\n")
    )
}

// ---------------------------------------------------------------------------
// TypeScript stubs (.d.ts), shared by Node and Deno (FR-9). Wired symbols only.
// ---------------------------------------------------------------------------

fn dts() -> String {
    let mut out = String::from("// DO NOT EDIT — generated by autoagent-bingen.\n");
    out.push_str("export class AutoAgentError extends Error { code: string; exitCode: number; }\n");
    for s in wired_symbols() {
        let params: Vec<String> = s
            .args
            .iter()
            .map(|a| format!("{}: {}", a.name, a.ty))
            .collect();
        let ret = s.returns.to_string();
        let ret = if matches!(s.kind, Kind::Async) {
            format!("Promise<{ret}>")
        } else {
            ret
        };
        out.push_str(&format!(
            "/** {} */\nexport function {}({}): {};\n",
            s.doc,
            camel(s.name),
            params.join(", "),
            ret
        ));
    }
    out
}

