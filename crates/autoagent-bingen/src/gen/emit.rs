// PEP 561 stubs (.pyi). Wired symbols only.
// ---------------------------------------------------------------------------

fn py_ret(s: &Symbol) -> &'static str {
    match classify(s) {
        Ret::Number => "int",
        Ret::Boolean => "bool",
        Ret::Void => "None",
        // JSON and raw text both cross to Python as a `str` (JSON-at-boundary).
        Ret::Str | Ret::Json => "str",
    }
}

fn pyi() -> String {
    let mut out = String::from("# DO NOT EDIT — generated by autoagent-bingen.\n");
    out.push_str("from typing import Any\n\n");
    out.push_str("class AutoAgentError(Exception):\n    code: str\n    exit_code: int\n\n");
    for s in wired_symbols() {
        let params: Vec<String> = s.args.iter().map(|a| format!("{}: str", a.name)).collect();
        out.push_str(&format!(
            "def {}({}) -> {}: ...\n",
            s.name,
            params.join(", "),
            py_ret(s)
        ));
    }
    out
}

