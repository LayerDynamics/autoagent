const NAPI_GLUE: &str = r#"use napi_derive::napi;
use crate::bind;

fn to_napi(e: bind::BindError) -> napi::Error {
    napi::Error::new(
        napi::Status::GenericFailure,
        format!("[{}|{}] {}", e.code, e.exit_code, e.message),
    )
}

fn parse(j: String) -> napi::Result<serde_json::Value> {
    serde_json::from_str(&j).map_err(|e| napi::Error::from_reason(e.to_string()))
}

"#;

fn napi_backend() -> String {
    let mut out = String::from(HEADER);
    out.push_str(NAPI_GLUE);
    for s in wired_symbols() {
        out.push_str(&napi_fn(s));
    }
    out
}

/// `name: Type` params for a napi function from the registry arg types.
fn napi_params(args: &[Arg]) -> String {
    args.iter()
        .map(|a| {
            let ty = match a.ty {
                "string | null" => "Option<String>",
                "boolean" => "bool",
                _ => "String",
            };
            format!("{}: {}", a.name, ty)
        })
        .collect::<Vec<_>>()
        .join(", ")
}

/// The `bind::name(<args>)` call expression from the registry arg names.
fn bind_call(s: &Symbol) -> String {
    let args = s
        .args
        .iter()
        .map(|a| match a.ty {
            "boolean" => a.name.to_string(),
            "string | null" => format!("{}.as_deref()", a.name),
            _ => format!("&{}", a.name),
        })
        .collect::<Vec<_>>()
        .join(", ");
    format!("bind::{}({})", s.name, args)
}

fn napi_fn(s: &Symbol) -> String {
    let rust = s.name; // snake_case Rust identifier
    let js = camel(s.name); // camelCase JS export
    let params = napi_params(s.args);
    let call = bind_call(s);
    match classify(s) {
        Ret::Number => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<u32> {{\n    {call}.map(|v| v.trim().parse().unwrap_or(0)).map_err(to_napi)\n}}\n\n"
        ),
        Ret::Boolean => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<bool> {{\n    {call}.map(|v| v.trim() == \"true\").map_err(to_napi)\n}}\n\n"
        ),
        Ret::Void => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<()> {{\n    {call}.map(|_| ()).map_err(to_napi)\n}}\n\n"
        ),
        Ret::Str => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<String> {{\n    {call}.map_err(to_napi)\n}}\n\n"
        ),
        Ret::Json => format!(
            "#[napi(js_name = \"{js}\", ts_return_type = \"{ret}\")]\npub fn {rust}({params}) -> napi::Result<serde_json::Value> {{\n    let j = {call}.map_err(to_napi)?;\n    parse(j)\n}}\n\n",
            ret = s.returns
        ),
    }
}

