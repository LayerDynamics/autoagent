//! Code emitters: turn the surface registry into backend source, type stubs,
//! a JSON schema, and package scaffolds. Each milestone widens [`WIRED`]; the
//! emitters are generic over a symbol's registry-declared args and return type,
//! so adding a backend or a symbol stays a single edit.
//!
//! `render_all` stays pure (no filesystem access) so it can be diffed by the
//! drift guard. Backends and stubs only ever emit *wired* symbols — a symbol is
//! wired once its neutral wrapper exists — so there are never declared-but-
//! unimplemented exports. The JSON schema, by contrast, always describes the
//! full surface contract (its purpose is to advertise intent to external tools).

use crate::bind::{Arg, Kind, Privilege, Symbol, SURFACE};
use std::collections::BTreeMap;

const HEADER: &str = "// DO NOT EDIT — generated by autoagent-bingen from bind.rs.\n";

/// Names of symbols whose neutral wrapper in `bind.rs` exists and is therefore
/// safe to expose through every backend + stub. Widened per milestone:
/// B1/B2 = read surface + `init`; B3 adds the mutating + async symbols.
const WIRED: &[&str] = &[
    "version",
    "doctor",
    "analyze",
    "init",
    "config_show",
    "patch_list",
    "patch_show",
    "memory_show",
    "tools_list",
];

fn is_wired(s: &Symbol) -> bool {
    WIRED.contains(&s.name)
}

fn wired_symbols() -> impl Iterator<Item = &'static Symbol> {
    SURFACE.iter().filter(|s| is_wired(s))
}

/// Every generated file as `relative_path -> content`. Pure: no I/O.
pub fn render_all() -> BTreeMap<String, String> {
    let mut m = BTreeMap::new();
    m.insert("src/node/napi.rs".into(), napi_backend());
    m.insert("src/python/pyrs.rs".into(), pyo3_backend());
    m.insert("dist/index.d.ts".into(), dts());
    m.insert("python/autoagent/__init__.pyi".into(), pyi());
    m.insert("schema/surface.schema.json".into(), schema_json());
    m
}

// ---------------------------------------------------------------------------
// Return-type classification (drives marshaling in every backend).
// ---------------------------------------------------------------------------

/// How a symbol's neutral-wrapper output (always a JSON string from `bind`) maps
/// to a host value.
enum Ret {
    /// `version` — parse the string as an integer.
    Number,
    /// `init` — parse the string as a boolean.
    Boolean,
    /// `void` — discard the (empty) string.
    Void,
    /// Raw text passthrough (e.g. rendered TOML, a patch body) — `returns: string`.
    Str,
    /// A JSON document (object/array/struct) — parse to a host value.
    Json,
}

fn classify(s: &Symbol) -> Ret {
    match s.returns {
        "number" => Ret::Number,
        "boolean" => Ret::Boolean,
        "void" => Ret::Void,
        "string" => Ret::Str,
        _ => Ret::Json,
    }
}

/// snake_case -> camelCase for JS/TS function names.
fn camel(snake: &str) -> String {
    let mut out = String::new();
    let mut up = false;
    for c in snake.chars() {
        if c == '_' {
            up = true;
        } else if up {
            out.extend(c.to_uppercase());
            up = false;
        } else {
            out.push(c);
        }
    }
    out
}

fn priv_str(p: Privilege) -> &'static str {
    match p {
        Privilege::Read => "read",
        Privilege::Mutate => "mutate",
    }
}

fn kind_str(k: Kind) -> &'static str {
    match k {
        Kind::Sync => "sync",
        Kind::Async => "async",
    }
}

// ---------------------------------------------------------------------------
// JSON schema of the full surface contract (FR-11).
// ---------------------------------------------------------------------------

fn schema_json() -> String {
    let syms: Vec<String> = SURFACE
        .iter()
        .map(|s| {
            let args: Vec<String> = s
                .args
                .iter()
                .map(|a| format!("{{ \"name\": \"{}\", \"type\": \"{}\" }}", a.name, a.ty))
                .collect();
            format!(
                "    {{ \"name\": \"{}\", \"kind\": \"{}\", \"privilege\": \"{}\", \"args\": [{}], \"returns\": \"{}\" }}",
                s.name,
                kind_str(s.kind),
                priv_str(s.privilege),
                args.join(", "),
                s.returns
            )
        })
        .collect();
    format!(
        "{{\n  \"version\": \"1.0.0\",\n  \"symbols\": [\n{}\n  ]\n}}\n",
        syms.join(",\n")
    )
}

// ---------------------------------------------------------------------------
// TypeScript stubs (.d.ts), shared by Node and Deno (FR-9). Wired symbols only.
// ---------------------------------------------------------------------------

fn dts() -> String {
    let mut out = String::from("// DO NOT EDIT — generated by autoagent-bingen.\n");
    out.push_str(
        "export class AutoAgentError extends Error { code: string; exitCode: number; }\n",
    );
    for s in wired_symbols() {
        let params: Vec<String> = s
            .args
            .iter()
            .map(|a| format!("{}: {}", a.name, a.ty))
            .collect();
        let ret = s.returns.to_string();
        let ret = if matches!(s.kind, Kind::Async) {
            format!("Promise<{ret}>")
        } else {
            ret
        };
        out.push_str(&format!(
            "/** {} */\nexport function {}({}): {};\n",
            s.doc,
            camel(s.name),
            params.join(", "),
            ret
        ));
    }
    out
}

// ---------------------------------------------------------------------------
// PEP 561 stubs (.pyi). Wired symbols only.
// ---------------------------------------------------------------------------

fn py_ret(s: &Symbol) -> &'static str {
    match classify(s) {
        Ret::Number => "int",
        Ret::Boolean => "bool",
        Ret::Void => "None",
        // JSON and raw text both cross to Python as a `str` (JSON-at-boundary).
        Ret::Str | Ret::Json => "str",
    }
}

fn pyi() -> String {
    let mut out = String::from("# DO NOT EDIT — generated by autoagent-bingen.\n");
    out.push_str("from typing import Any\n\n");
    out.push_str("class AutoAgentError(Exception):\n    code: str\n    exit_code: int\n\n");
    for s in wired_symbols() {
        let params: Vec<String> = s.args.iter().map(|a| format!("{}: str", a.name)).collect();
        out.push_str(&format!("def {}({}) -> {}: ...\n", s.name, params.join(", "), py_ret(s)));
    }
    out
}

// ---------------------------------------------------------------------------
// napi-rs backend (Node primary). Wired symbols only.
// ---------------------------------------------------------------------------

const NAPI_GLUE: &str = r#"use napi_derive::napi;
use crate::bind;

fn to_napi(e: bind::BindError) -> napi::Error {
    napi::Error::new(
        napi::Status::GenericFailure,
        format!("[{}|{}] {}", e.code, e.exit_code, e.message),
    )
}

fn parse(j: String) -> napi::Result<serde_json::Value> {
    serde_json::from_str(&j).map_err(|e| napi::Error::from_reason(e.to_string()))
}

"#;

fn napi_backend() -> String {
    let mut out = String::from(HEADER);
    out.push_str(NAPI_GLUE);
    for s in wired_symbols() {
        out.push_str(&napi_fn(s));
    }
    out
}

/// `name: Type` params for a napi function from the registry arg types.
fn napi_params(args: &[Arg]) -> String {
    args.iter()
        .map(|a| {
            let ty = match a.ty {
                "string | null" => "Option<String>",
                "boolean" => "bool",
                _ => "String",
            };
            format!("{}: {}", a.name, ty)
        })
        .collect::<Vec<_>>()
        .join(", ")
}

/// The `bind::name(<args>)` call expression from the registry arg names.
fn bind_call(s: &Symbol) -> String {
    let args = s
        .args
        .iter()
        .map(|a| match a.ty {
            "boolean" => a.name.to_string(),
            "string | null" => format!("{}.as_deref()", a.name),
            _ => format!("&{}", a.name),
        })
        .collect::<Vec<_>>()
        .join(", ");
    format!("bind::{}({})", s.name, args)
}

fn napi_fn(s: &Symbol) -> String {
    let rust = s.name; // snake_case Rust identifier
    let js = camel(s.name); // camelCase JS export
    let params = napi_params(s.args);
    let call = bind_call(s);
    match classify(s) {
        Ret::Number => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<u32> {{\n    {call}.map(|v| v.trim().parse().unwrap_or(0)).map_err(to_napi)\n}}\n\n"
        ),
        Ret::Boolean => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<bool> {{\n    {call}.map(|v| v.trim() == \"true\").map_err(to_napi)\n}}\n\n"
        ),
        Ret::Void => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<()> {{\n    {call}.map(|_| ()).map_err(to_napi)\n}}\n\n"
        ),
        Ret::Str => format!(
            "#[napi(js_name = \"{js}\")]\npub fn {rust}({params}) -> napi::Result<String> {{\n    {call}.map_err(to_napi)\n}}\n\n"
        ),
        Ret::Json => format!(
            "#[napi(js_name = \"{js}\", ts_return_type = \"{ret}\")]\npub fn {rust}({params}) -> napi::Result<serde_json::Value> {{\n    let j = {call}.map_err(to_napi)?;\n    parse(j)\n}}\n\n",
            ret = s.returns
        ),
    }
}

// ---------------------------------------------------------------------------
// pyo3 backend (Python primary). Wired symbols only.
// ---------------------------------------------------------------------------

const PYO3_GLUE: &str = r#"use pyo3::prelude::*;
use crate::bind;

pyo3::create_exception!(autoagent, AutoAgentError, pyo3::exceptions::PyException);

fn to_py(e: bind::BindError) -> PyErr {
    AutoAgentError::new_err(format!("[{}|{}] {}", e.code, e.exit_code, e.message))
}

"#;

fn pyo3_backend() -> String {
    let mut out = String::from(HEADER);
    out.push_str(PYO3_GLUE);
    let mut registrations = String::new();
    for s in wired_symbols() {
        out.push_str(&pyo3_fn(s));
        registrations.push_str(&format!(
            "    m.add_function(wrap_pyfunction!({}, m)?)?;\n",
            s.name
        ));
    }
    out.push_str(&format!(
        "#[pymodule]\nfn autoagent(m: &Bound<'_, PyModule>) -> PyResult<()> {{\n    m.add(\"AutoAgentError\", m.py().get_type::<AutoAgentError>())?;\n{registrations}    Ok(())\n}}\n"
    ));
    out
}

/// `name: Type` params for a pyo3 function.
fn pyo3_params(args: &[Arg]) -> String {
    args.iter()
        .map(|a| {
            let ty = match a.ty {
                "string | null" => "Option<String>",
                "boolean" => "bool",
                _ => "String",
            };
            format!("{}: {}", a.name, ty)
        })
        .collect::<Vec<_>>()
        .join(", ")
}

fn pyo3_fn(s: &Symbol) -> String {
    let name = s.name;
    let params = pyo3_params(s.args);
    let call = bind_call(s);
    match classify(s) {
        Ret::Number => format!(
            "#[pyfunction]\nfn {name}({params}) -> PyResult<u32> {{\n    {call}.map(|v| v.trim().parse().unwrap_or(0)).map_err(to_py)\n}}\n\n"
        ),
        Ret::Boolean => format!(
            "#[pyfunction]\nfn {name}({params}) -> PyResult<bool> {{\n    {call}.map(|v| v.trim() == \"true\").map_err(to_py)\n}}\n\n"
        ),
        Ret::Void => format!(
            "#[pyfunction]\nfn {name}({params}) -> PyResult<()> {{\n    {call}.map(|_| ()).map_err(to_py)\n}}\n\n"
        ),
        // JSON and raw text both return as a Python `str` (JSON-at-boundary).
        Ret::Str | Ret::Json => format!(
            "#[pyfunction]\nfn {name}({params}) -> PyResult<String> {{\n    {call}.map_err(to_py)\n}}\n\n"
        ),
    }
}
