//! Code emitters: turn the surface registry into backend source, type stubs,
//! a JSON schema, and package scaffolds. Each milestone widens [`WIRED`]; the
//! emitters are generic over a symbol's registry-declared args and return type,
//! so adding a backend or a symbol stays a single edit.
//!
//! `render_all` stays pure (no filesystem access) so it can be diffed by the
//! drift guard. Backends and stubs only ever emit *wired* symbols — a symbol is
//! wired once its neutral wrapper exists — so there are never declared-but-
//! unimplemented exports. The JSON schema, by contrast, always describes the
//! full surface contract (its purpose is to advertise intent to external tools).

use crate::bind::{Arg, Kind, Privilege, Symbol, SURFACE};
use std::collections::BTreeMap;

const HEADER: &str = "// DO NOT EDIT — generated by autoagent-bingen from bind.rs.\n";

/// Names of symbols whose neutral wrapper in `bind.rs` exists and is therefore
/// safe to expose through every backend + stub. Widened per milestone:
/// B1/B2 = read surface + `init`; B3-T3 adds the sync mutating surface
/// (`apply`, `revert`, `run_sync`, `evolve_sync`). The async `run`/`evolve`
/// (Promise/awaitable) stay unwired until their bridge compiles + loads.
const WIRED: &[&str] = &[
    "version",
    "doctor",
    "analyze",
    "init",
    "config_show",
    "patch_list",
    "patch_show",
    "memory_show",
    "tools_list",
    "apply",
    "revert",
    "run_sync",
    "evolve_sync",
];

fn is_wired(s: &Symbol) -> bool {
    WIRED.contains(&s.name)
}

fn wired_symbols() -> impl Iterator<Item = &'static Symbol> {
    SURFACE.iter().filter(|s| is_wired(s))
}

/// Every generated file as `relative_path -> content`. Pure: no I/O.
pub fn render_all() -> BTreeMap<String, String> {
    let mut m = BTreeMap::new();
    m.insert("src/node/napi.rs".into(), napi_backend());
    m.insert("src/node/node_bindgen.rs".into(), node_bindgen_backend());
    m.insert("src/python/pyrs.rs".into(), pyo3_backend());
    m.insert("src/python/python_bingen.rs".into(), rustpython_backend());
    m.insert("src/deno/ffi.rs".into(), ffi_backend());
    m.insert("src/deno/deno_bindgen.rs".into(), deno_bindgen_backend());
    m.insert("deno/mod.ts".into(), deno_mod_ts());
    m.insert("dist/index.d.ts".into(), dts());
    m.insert("python/autoagent/__init__.pyi".into(), pyi());
    m.insert("schema/surface.schema.json".into(), schema_json());
    m.insert("package.json".into(), package_json());
    m.insert("deno.json".into(), deno_json());
    m.insert("dist/index.js".into(), node_loader_js());
    m
}

