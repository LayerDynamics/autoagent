/// How a symbol's neutral-wrapper output (always a JSON string from `bind`) maps
/// to a host value.
enum Ret {
    /// `version` — parse the string as an integer.
    Number,
    /// `init` — parse the string as a boolean.
    Boolean,
    /// `void` — discard the (empty) string.
    Void,
    /// Raw text passthrough (e.g. rendered TOML, a patch body) — `returns: string`.
    Str,
    /// A JSON document (object/array/struct) — parse to a host value.
    Json,
}

fn classify(s: &Symbol) -> Ret {
    match s.returns {
        "number" => Ret::Number,
        "boolean" => Ret::Boolean,
        "void" => Ret::Void,
        "string" => Ret::Str,
        _ => Ret::Json,
    }
}

/// snake_case -> camelCase for JS/TS function names.
fn camel(snake: &str) -> String {
    let mut out = String::new();
    let mut up = false;
    for c in snake.chars() {
        if c == '_' {
            up = true;
        } else if up {
            out.extend(c.to_uppercase());
            up = false;
        } else {
            out.push(c);
        }
    }
    out
}

fn priv_str(p: Privilege) -> &'static str {
    match p {
        Privilege::Read => "read",
        Privilege::Mutate => "mutate",
    }
}

fn kind_str(k: Kind) -> &'static str {
    match k {
        Kind::Sync => "sync",
        Kind::Async => "async",
    }
}

